import { create } from 'zustand';

interface AuthStore {
  username: string;
  login: (username: string) => void;
  logout: () => void;
}

export const useAuthStore = create<AuthStore>((set) => ({
  username: '',
  login: (username) => set(() => ({ username: username })),
  logout: () => set(() => ({ username: '' })),
}));
